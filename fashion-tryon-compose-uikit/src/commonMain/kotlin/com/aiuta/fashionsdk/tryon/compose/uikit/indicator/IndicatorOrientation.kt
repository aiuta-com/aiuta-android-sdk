package com.aiuta.fashionsdk.tryon.compose.uikit.indicator

/**
 * Represents the orientation of a page indicator.
 * This enum is used to determine whether the indicator should be displayed horizontally or vertically.
 */
public enum class IndicatorOrientation {
    /**
     * The indicator is displayed horizontally, with pages arranged from left to right.
     */
    HORIZONTAL,

    /**
     * The indicator is displayed vertically, with pages arranged from top to bottom.
     */
    VERTICAL,
}
