package com.aiuta.fashionsdk.configuration.ui.theme.color

/**
 * Color theme scheme options for the Aiuta SDK.
 * 
 * This enum defines the available color schemes that can be used to style the SDK's
 * user interface. The color scheme determines the overall appearance of the UI.
 * 
 * 
 * @property LIGHT Light color scheme with light backgrounds and dark text
 * @property DARK Dark color scheme with dark backgrounds and light text
 */
public enum class AiutaColorThemeScheme {
    LIGHT,
    DARK,
}
