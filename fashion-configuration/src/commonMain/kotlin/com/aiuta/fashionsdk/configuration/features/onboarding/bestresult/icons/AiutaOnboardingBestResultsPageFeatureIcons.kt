package com.aiuta.fashionsdk.configuration.features.onboarding.bestresult.icons

import com.aiuta.fashionsdk.compose.resources.drawable.AiutaIcon

/**
 * Interface defining icons used in the best results page of the onboarding flow.
 *
 * This interface provides icons for indicating good and bad examples in the
 * best results showcase.
 */
public interface AiutaOnboardingBestResultsPageFeatureIcons {
    /**
     * 24x24 pixel icon used to indicate a good example in the best results showcase.
     */
    public val onboardingBestResultsGood24: AiutaIcon

    /**
     * 24x24 pixel icon used to indicate a bad example in the best results showcase.
     */
    public val onboardingBestResultsBad24: AiutaIcon
}
